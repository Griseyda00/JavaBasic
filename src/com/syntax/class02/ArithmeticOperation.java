package com.syntax.class02;

public class ArithmeticOperation {
public static void main(String[] args) {
	double a=12.3;
	double b=12.3;
	System.out.println("The sum of 12.33 and 12.3 is "+ (a+b));
	System.out.println("a-b="+ (a-b));
	System.out.println("a*b="+ (a*b));
	System.out.println("a/b="+ (a/b));
	//always use parenthesis
	
		
	
}
}
