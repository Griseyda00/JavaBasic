package com.syntax.class07;

public class WhilevsDoWhile {
	public static void main(String[] args) {
		int num = 11;
		while (num <= 5) {
			System.out.println("hello");
			num++;
		}
		System.out.println("USING DO WHILE LOOP");
        int num1=11;
        do {
        	System.out.println("hello");
        	num1++;
        }while(num<=5); 
        
	}
}
