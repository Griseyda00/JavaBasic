package com.syntax.class07;

public class whileLoop {
	public static void main(String[] args) {
		// how to print numbers 1-50
		int num = 1;
		while (num <= 50) {
			System.out.println(num);
			num++;
		}
		int num1 = 20;
		while (num <= 30) {
			System.out.println(num1);
			num1++;
		}
		int num2=10;
		while (num2>=1) {
			System.out.println(num2);
			num2--;
		}
	}
}
